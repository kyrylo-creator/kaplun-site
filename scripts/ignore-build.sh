#!/usr/bin/env bash
# Netlify "ignore" команда (netlify.toml → [build] ignore).
# Код виходу 0 = ПРОПУСТИТИ білд; будь-який інший код = ЗІБРАТИ сайт.
#
# Навіщо: Decap CMS робить окремий git-комміт на кожен збережений відгук/статтю.
# Раніше кожен такий комміт запускав повну збірку — пачка контенту з'їдала
# місячний ліміт білдів Netlify. Тепер збірка запускається однією кнопкою
# «Опублікувати зміни» в /admin (через Netlify Build Hook).
#
# Правила:
#   1) Білд запущено кнопкою (Build Hook) → збираємо завжди.
#   2) У пуші змінився лише контент CMS (src/reviews/, src/posts/) → пропускаємо.
#   3) Змінилося щось поза цими теками (код, верстка) → збираємо одразу.

# 1) Кнопка «Опублікувати»: Netlify виставляє INCOMING_HOOK_TITLE для білд-хука.
if [ -n "$INCOMING_HOOK_TITLE" ]; then
  echo "Build Hook (кнопка «Опублікувати») → збираємо."
  exit 1
fi

# 2/3) Порівнюємо попередньо зібраний комміт із поточним, ігноруючи контент CMS.
# git diff --quiet: код 0 = різниці немає, код 1 = різниця є.
if git diff --quiet "$CACHED_COMMIT_REF" "$COMMIT_REF" -- . ':(exclude)src/reviews' ':(exclude)src/posts'; then
  echo "Змінився лише контент CMS → пропускаємо білд (натисніть «Опублікувати» в /admin)."
  exit 0
fi

echo "Є зміни поза контентом CMS → збираємо."
exit 1
